void LCD_P6x8Str(byte x,byte y,byte ch[],byte b)
{
	unsigned char c=0,i=0,j=0;      
	while (ch[j]!='\0')
	{    
		c =ch[j]-32;
		for(i=0;i<6;i++)   
		{
			if(b)
				LCD_Buffer[x][y]=(F6x8[c][i]);  
			else
				LCD_Buffer[x][y]=(~F6x8[c][i])&LCD_Buffer[x][y];
			x++;
		}
		j++;
	}
}

void LCD_P8x16Str(unsigned char x,unsigned char y,unsigned char ch[])
{
	unsigned char c=0,i=0,j=0;
	
	while (ch[j]!='\0')
	{    
		c =ch[j]-32;
		if(x>120){x=0;y++;}
		LCD_Set_Pos(x,y);    
		for(i=0;i<8;i++)     
			LCD_WrDat(F8X16[c*16+i]);
		LCD_Set_Pos(x,y+1);    
		for(i=0;i<8;i++)     
			LCD_WrDat(F8X16[c*16+i+8]);  
		x+=8;
		j++;
	}
}

void Draw_BMP(byte bmp[])
{ 	
	byte x,y;
	word ii=0;
	
	for(x=0;x<128;x++)
		for(y=0;y<8;y++)
		{
			LCD_Set_Pos(x,y);	
			LCD_WrDat(bmp[ii++]);
		}
}
void LCD_W_BMP(byte x,byte y,byte L,byte W,byte bmp[],byte b)
{
	byte x0,y0,Wei=0;
	word ii=0;
	for(x0=x;x0<x+L;x0++)
		for(y0=y;y0<y+W;y0++)
		{
				if((bmp[ii]>>Wei)&0x01)
					if(b)
						LCD_W_Pos(x0,y0,1);
					else
						LCD_W_Pos(x0,y0,0);
			Wei++;
			if(Wei==8)
			{
				Wei=0;
				ii++;
			}
		}	
}
//m^n
word LCD_Pow(byte m,byte n)
{
	word result=1;	 
	while(n--)result*=m;    
	return result;
}	

